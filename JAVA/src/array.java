public class array {
    public static void main(String[] args) {
        int ar[]={11,22,33,44,55,};
        System.out.println("______array element____");
        for (int i = 0; i < ar.length; i++)
        {
            System.out.println(ar[i]);
        }
        System.out.println("No. of elements : "+ar.length);
    }
}
